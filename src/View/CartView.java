    private void btnOrderNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderNowActionPerformed
        // TODO add your handling code here:
        
        String Uname=labUname.getText().trim();
        try 
        {
            Statement st=MyDBConn.getMyDBCon().createStatement();
            
            ResultSet resultSet = st.executeQuery("SELECT COUNT(*) FROM Cart WHERE Username='"+Uname+"'");
            resultSet.next(); 
            
            int rowCount = resultSet.getInt(1);

            if (rowCount > 0) 
            { 
                try
                {
                    Statement st1 = MyDBConn.getMyDBCon().createStatement(); 
                    String sql = "SELECT SUM(ItemPrice) FROM Cart WHERE Username='"+Uname+"'";
                    ResultSet rs = st1.executeQuery(sql);

                    if (rs.next()) 
                    {
                        double total = rs.getDouble(1); 
                        
                        OrderView OV = new OrderView();
                        OV.labTotal.setText(String.valueOf(total));
                        OV.labUname.setText(Uname);
                        OV.setVisible(true);
                        dispose();
                    }

                    rs.close();
                    st1.close();
                }
                catch (Exception e) 
                {
                    JOptionPane.showMessageDialog(null,e.getMessage());
                }
               
            } 
            else 
            {
                JOptionPane.showMessageDialog(null,"Your cart is already empty.");
            }
            resultSet.close();
        } 
        
        catch (Exception e) 
        {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_btnOrderNowActionPerformed

