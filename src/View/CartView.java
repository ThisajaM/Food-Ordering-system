    private void btnViewCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewCartActionPerformed
        // TODO add your handling code here:
        String Uname=labUname.getText().trim();
        
        try
        {
            Statement st=MyDBConn.getMyDBCon().createStatement();
            String sql="SELECT * from Cart WHERE Username='"+Uname+"'";
            ResultSet rs=st.executeQuery(sql);
            
            while(rs.next())
            {
                String ItemName = rs.getString("ItemName");
                String ItemQty = String.valueOf(rs.getInt("ItemQty"));
                String ItemPrice = String.valueOf(rs.getInt("ItemPrice"));
                
                String Cart[]={ItemName,ItemQty,ItemPrice};
                DefaultTableModel tbl = (DefaultTableModel) tabCart.getModel();
                
                tbl.addRow(Cart);
            }
        }
        catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
        
        btnViewCart.setEnabled(false);
    }//GEN-LAST:event_btnViewCartActionPerformed

